package src;

public class Day4_SumProduct3Number {
    public static void main(String[] args) {
        int number1 = 98;
        int number2 = 5;
        int number3 = 77;

        int sum = number1 + number2 + number3;
        int product = number1 * number2 * number3;
        System.out.println("Sum = " + sum);
        System.out.println("Product = " + product);
    }
}
